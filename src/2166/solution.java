public class Solution {
    public int addition(int a, int b) {
        return a+b;
    }

    public int subtraction(int a, int b) {
        return a-b;
    }

    public int multiplication(int a, int b) {
        return a*b;
    }

    public float division(int a, int b) {
        return (float)a/(float)b;
    }
}
